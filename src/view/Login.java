    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            String user = txtUser.getText();
            String password = String.valueOf(txtPassword.getPassword());

            if (user.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please input your username.", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (password.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please input your Password.", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                ResultSet resultSet = MySQL.execute("SELECT * FROM `staff` INNER JOIN `roles` ON `staff`.`roles_role_id`=`roles`.`role_id` WHERE `username`='" + user + "' AND `password`='" + password + "' AND `status_id`='1'");
                if (resultSet != null && resultSet.next()) {
                    LogedUserBean userBean = new LogedUserBean();
                    userBean.setStaffId(resultSet.getInt("staff_id"));
                    userBean.setUsername(user);
                    userBean.setFname(resultSet.getString("first_name"));
                    userBean.setLname(resultSet.getString("last_name"));
                    userBean.setStaffRole(resultSet.getString("roles_role_id"));
                    userBean.setStaffRole(resultSet.getString("role_name"));
                    JOptionPane.showMessageDialog(this, "Login Success", "Info", JOptionPane.INFORMATION_MESSAGE);
                    
                    Home home = new Home(userBean);
                    home.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Invalid Username or Password.", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnLoginActionPerformed

